/**
 * Sales order view items block
 *
 * @author     Magento Core Team <core@magentocommerce.com>
 */
namespace Dealer4Dealer\SubstituteOrders\Block\Order;

class Items extends \Magento\Sales\Block\Items\AbstractItems
{
    /**
     * Core registry
     *
     * @var \Magento\Framework\Registry
     */
    protected $_coreRegistry = null;

    /**
     * @param \Magento\Framework\View\Element\Template\Context $context
     * @param \Magento\Framework\Registry $registry
     * @param array $data
     */
    public function __construct(
        \Magento\Framework\View\Element\Template\Context $context,
        \Magento\Framework\Registry $registry,
        array $data = []
    ) {
        $this->_coreRegistry = $registry;
        parent::__construct($context, $data);
    }

    /**
     * Retrieve current order model instance
     *
     * @return \Magento\Sales\Model\Order
     */
    public function getOrder()
    {
        return $this->_coreRegistry->registry('current_order');
    }

    /**
     * Get html of order attachment block
     *
     * @param   \Magento\Sales\Model\Order $order
     * @return  string
     */
    public function getOrderAttachmentHtml($order)
    {
        $html = '';
        $comments = $this->getChildBlock('order_attachments');
        if ($comments) {
            $comments->setEntity($order)->setTitle(__('Order Attachments'));
            $html = $comments->toHtml();
        }
        return $html;
    }
}
